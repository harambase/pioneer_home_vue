                            情感：特别是自信和自尊。设立现实的目标；对付困惑失望，欣赏成功；恰当地表达情感并在必要时寻求帮助；识别对学习造成潜在阻力的情绪和危机；应对成功和失败，从判断失误中吸取教训；找到解决个人问题的方法
                          </p>
