                          <p>
                            进入90年代以来，项目教学又再度引起人们的重视，我们不得不把这种现象与信息时代的要求和现代教育的新思潮所联系起来。信息时代的一个重要特征就是人们借助信息技术工具就可以随时随地获得知识，而教育的普及几乎使得人人都可以受到基础的教育。传统的教育方式通常是抹杀个性，忽视差异，让同一个年龄段的孩子，在同样的时间，以同样的方式，学同样的内容。可悲的是这种学习方式常常扼杀了学生的学习兴趣，抑制了学生的创造力。因此，社会对针对个体学习需求的个性化的，高品质的教育的呼声也日趋高涨。与此同时，越来越多的教育工作者在如何对儿童进行适宜的早期教育方面达成了越来越多的共识</p>
