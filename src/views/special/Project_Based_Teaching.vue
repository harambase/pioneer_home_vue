                        <p><strong>１．选题</strong></p>
                        <p>
                          主题活动和项目活动一开始都要选择一个题目作为自己的“主题”或“课题”的名称。然而主题通常是教师事先计划好的，或者孩子的兴趣点与老师的思路不谋而合，且是教师认为有教育意义和教育价值的的（尽管近来人们越来越重视要跟孩子的兴趣挂钩）。而项目的课题的选定，是在孩子的日常活动中，从孩子的兴趣为出发点而自然生成的。此外，任何一个项目都可以作为主题活动开展，但是所有的主题活动不一定可以做成项目活动。</p>
                        <p>项目课题选择的几个重要标准是：</p>
                        <p>项目必须是孩子真正感兴趣的；<br>
                          必须是在孩子的某些亲身经历范围内的；真实世界的，孩子可以直接观察，实地考察的。能让孩子获得一手资料的;<br>
                          项目活动计划应当在孩子的头脑里，从与教师共同进行项目逐步过渡到到孩子自己确定项目，按自己的计划自主探究学习，获得学习能力。</p>
                        <p><strong>２．活动的组织</strong></p>
                        <p>
                          主题活动指的是围绕某个大的课题或概念（如“春天”或“秋季”）组织的一组系列活动。孩子通常是根据自己的年龄特征做一些与主题有关的活动，其中包括点数、测量、画画、表演等等。这样主题是用来为应用各种各样的技能作前期铺垫的。其目的是找到一个主题，围绕主题应用某些基本技能并做一些美术和手工活动。这样以来，围绕一个主题而进行的组织工作通常是寻找与之相关的事物来组织一个一个的活动。相反，在项目活动中，随着调查研究工作的进展，孩子们要学会收集数据来回答自己所研究的问题，他们要寻找可以恰当呈现自己学习的体验和发现的表达方式，如用绘画、剪纸、面塑、图表、搭建、表演、个人和班级编的书、报告等等。</p>
                        <p><strong>３．直接体验</strong></p>
                        <p>
                          项目活动最最强调的一点就是实地考查是必不可少的重要环节。在这里我们不应该把考查片面地理解为只是参观“看看”而已，而应该是孩子调动所有身体感官的“亲身体验”。这种体验应是全方位的，除了用眼“看看”以外，还要用手“摸摸”，用耳“听听”，用鼻“闻闻”，用嘴“尝尝”。而在内地地区幼儿园经常进行的“神奇的海洋世界”这类的主题活动，虽然也是组织孩子进行考察，只不过是到海洋博物馆或水族馆，隔着玻璃亲眼看看而已，更多的知识是通过图书、录象这种“二手资源”获得的相关正确知识和答案，无充分深刻“体验”可言。项目活动把直接体验、一手经验始终放在第一位，孩子年龄越小，就越应该如此。
                        </p>
                        <p>
                          与主题活动的区别。项目活动是儿童要对某个课题进行深入的研究。（这个课题也可能与一个大的题目有关。）在项目活动中，孩子的想法、问题、理论、预测、兴趣是其体验和学习成效的决定因素。对孩子在项目活动中所留下的档案进行仔细研究就可发现，孩子们频繁地对一些事情作出决定，对某个问题进行争论，向全班同学对自己的想法作出解释，对可能的发现作出预测，对预测的依据提出假设，对事实和细节进行查证，面访专家，提出新的方向进行探索，根据记忆、想象和实际观察进行绘画，记录观察结果，报告新发现，相互提出建议，相互鼓励，一再进行尝试并用很多方式来做，接受并承担自己工作的责任。</p>
                        <p><strong>５．时间</strong></p>
                        <p>从活动进行的时间跨度来看，项目活动比较灵活。短则２－３个星期，长则可长达２－３个月。</p>
                        <p><strong>6、活动形式</strong></p>
                        <p>
                          项目活动主要是儿童对研究的内容有相同的兴趣，根据自己要解决的问题组成小组或个体进行，各组儿童研究的内容不尽相同。因此儿童就有更多的选择，自己做出决定，参加哪个组的活动，探究哪方面的内容，解决什么问题。</p>
                        <p>
                          主题活动常常是全体儿童集体进行同样的活动，完成一个活动再进行另一个活动。这些活动基本按五大领域的内容组织实施。主题活动结束的呈现方式大都是儿童美术活动的结果，画画或制作。当进行另一个主题活动时，不过名称和内容进行了改变。</p>
