                            近年来，由于信息时代给社会带来的巨大冲击，欧美各国以及世界很多地方都面临教育改革的呼声。教育工作者常常在究竟是采用传统的学术化的课程，还是采用社会化的游戏化的课程方面进退两难。两种方式似乎各有千秋，又有明显的不足。而项目教学恰恰使这两种截然相反的教学方式找到了很好的平衡点。因为相对传统的学术性的教学而言，项目活动则更加灵活，更具游戏性；而相对自主性游戏而言，项目活动则更具结构性，从而更加靠向学术性一端。那么究竟什么是项目教学呢？
