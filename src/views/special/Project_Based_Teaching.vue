                          <p class="left">项目教学的特点 speciality</p>
                          <p class="right">
                            根据以上对项目教学的概述，我们不难看出项目教学首先是非常强调跟现实生活的联系，它是一种让学生从亲身经历中获得经验和学习的以学生为本的教育活动；第二，项目教学特别注重观察能力和思维能力的培养，它是属于基于问题的学习，是非常适宜孩童的一种行之有效的研究性学习；第三，幼儿的项目活动几乎都是小组协作完成的，它非常有利于培养儿童的协作性和社会交往能力；第四，项目教学法虽然提供了一个大致的框架结构，但它更像一套灵活的教育策略，而不是要人按部就班的“法”，老师在教学实施过程中有充分的余地发挥自己的创造性；家长的参与，实地的参观采访等使课堂内外有机地结合为一个整体，一起构建了一个全新的学习环境。
