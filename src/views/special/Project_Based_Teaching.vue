                          目前我国的很多幼儿园都开展主题活动，教师开展项目活动的时候常常把二者混淆，弄不清自己做的究竟是项目还是主题。诚然，从目前幼儿园开展的各种教学活动来看，主题活动是较接近于项目活动的一种形式，二者从表面上看来似乎颇多相似之处，然而，只要仔细对照，其差异也是显而易见的。我们不仿作一番比较。
